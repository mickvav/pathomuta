  `FILEID` INT,
  `CHROM` VARCHAR(1000) COMMENT 'Chromosome',
  `POS` INT COMMENT 'Position',
  `ID`  VARCHAR(1000) COMMENT 'Identifier',
  `REF` VARCHAR(1000) COMMENT 'Reference base(s)',
  `ALT` VARCHAR(1000) COMMENT 'Alternate base(s)',
  `QUAL` FLOAT COMMENT 'Quality',
  `FILTER` VARCHAR(1000) COMMENT 'Filter status',
